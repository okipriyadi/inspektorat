            <h4>Pembahasan Prosedur Pengangkatan Pertama Jabatan Fungsional Auditor dan Pembentukan Tim Penilaian Angka Kredit Pada Inspektorat Kementerian PANRB</h4>
            <small>Rabu, 6 Mei 2020</small>
        </div>
        <div class="col-sm-12">
            <p>Teleconference melalui aplikasi Zoom mengundang Bapak Drs. Fauzi,M.M dan Bapak Sofyan Antonius, Ak. MM, CA dari Pusat Pembinaan Jabatan Fungsional Auditor (Pusbin JFA) untuk membahas mengenai Prosedur Pengangkatan Pertama Jabatan Fungsional Auditor dan Pembentukan Tim Penilaian Angka Kredit di Inspektorat Kementerian PANRB. Adapun beberapa poin penting dari kegiatan ini yaitu :</p>
            <p>1. Permenpan 220 tahun 2008 tentang Jabatan Fungsional Auditor Dan Angka Kreditnya masih berlaku namun perlu dilakukan revisi karena telah terbit Permenpan 13 Tahun 2019 tentang Pengusulan Penetapan Dan Pembinaan Jabatan Fungsional Pegawai Negeri Sipil.</p>
            <p>2. Syarat pengangkatan jabatan fungsional auditor untuk pertama kali yaitu berstatus sebagai PNS, syarat pendidikan terpenuhi, nilai prestasi kerja minimal satu tahun terakhir baik dan dalam3 tahun wajib mengikuti diklat auditor.</p>
            <p>3. Untuk pengangkatan pertama dari status CPNS, setelah diangkat menjadi PNS dapat mengajukan pertimbangan teknis (pertek) ke BPKP untuk mendapatkan surat persetujuan pengangkatan JFA beserta pengajuan angka kredit untuk kemudian dilakukan verifikasi oleh BPKP untuk diberikan persetujuan pengangkatan.</p>
            <p>4. Syarat pengangkatan perpindahan jabatan fungsional auditor yaitu lulus uji kompetensi inpassing, pengalaman kerja minimal 2 tahun, batas usia keahlian (53 tahun untuk JFA Pertama dan Muda, 55 tahun untuk JFA Madya, 60 tahun untuk JFA Ahli Utama bagi yang menduduki JPT), Batas usia keterampilan yaitu 53 tahun dan batas usia pindah ahli utama yaitu 63 tahun.</p>
            <p>5. Untuk K/L yang belum memiliki tim penilai Angka Kredit dapat menitipkan penilaian Angka Kredit di Pusbin JFA BPKP apabila tidak dapat melakukan penilaian gabungan dengan K/L lain, namun hanya bersifat sementara sampai terbentuk tim penilaian angka kredit.</p>
            <p>6. Saat penilaian pertama Angka Kredit oleh Tim Penilai Angka Kredit untuk mengantisipasi Subjektifitas, BPKP dapat melakukan pendampingan dalam penilaian oleh Tim Penilai.</p>
            <p>7. Untuk Formasi D3 yang sedang menempuh kuliah S1 apa bisa pindah jalur langsung ke Auditor Pertama, menurut BPKP izin belajar nantinya akan diakui secara kedinasan kemudian mengikuti ujian pindah jalur, setelah itu baru bisa pindah menjadi Auditor Pertama.</p>
            <p>8. Keanggotaan tim penilai min 7 orang yaitu Ketua merangkap anggota, Wakil ketua merangkap anggota, Sekre merangkap anggota, Dari 4 orang anggota minimal 2 orang auditor dengan catatan Inspektur tidak dapat menjadi ketua tim penilaian angka kredit. Melihat kondisi di Inspektorat Kementerian PANRB jabatan tertinggi Auditornya adalah 3A maka ketua tim dapat berasal dari kepegawaian untuk jabatan yg tertingginya.</p>
